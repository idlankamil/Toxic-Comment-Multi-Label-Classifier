# Feature engineering function
def add_features(df):
    df['char_count'] = df['comment_text'].apply(len)
    df['word_count'] = df['comment_text'].apply(lambda x: len(x.split()))
    df['avg_word_length'] = df['char_count'] / (df['word_count'] + 1e-5)
    df['exclamation_count'] = df['comment_text'].apply(lambda x: x.count('!'))
    df['uppercase_ratio'] = df['comment_text'].apply(lambda x: sum(1 for c in x if c.isupper()) / len(x) if len(x) > 0 else 0)
    df['polarity'] = df['comment_text'].apply(lambda x: TextBlob(x).sentiment.polarity)
    df['subjectivity'] = df['comment_text'].apply(lambda x: TextBlob(x).sentiment.subjectivity)

    # VADER Sentiment Analysis Features
    df['vader_polarity'] = df['comment_text'].apply(lambda x: analyzer.polarity_scores(x)['compound'])
    df['vader_neg'] = df['comment_text'].apply(lambda x: analyzer.polarity_scores(x)['neg'])
    df['vader_neu'] = df['comment_text'].apply(lambda x: analyzer.polarity_scores(x)['neu'])
    df['vader_pos'] = df['comment_text'].apply(lambda x: analyzer.polarity_scores(x)['pos'])

    return df

