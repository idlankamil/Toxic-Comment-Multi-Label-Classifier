# --------------------------- Combination of Oversampling and Undersampling ---------------------------
def resample_multilabel(X, y, smote_enn):
    """
    Resamples multilabel data using SMOTEENN for all labels simultaneously.
    Ensures binary labels in the output.
    """
    # Combine features and labels into a single dataset for consistent sampling
    combined = np.hstack((X, y.to_numpy()))  # Combine X and y into a single array

    # Apply SMOTEENN
    print("Applying SMOTEENN for multilabel data...")
    combined_resampled, _ = smote_enn.fit_resample(combined, y.sum(axis=1))  # Use label sum for SMOTEENN

    # Split the resampled dataset back into features and labels
    X_resampled = combined_resampled[:, :X.shape[1]]
    y_resampled = combined_resampled[:, X.shape[1]:]

    # Convert y_resampled back to binary (0 or 1)
    y_resampled = (y_resampled > 0.5).astype(int)

    return X_resampled, y_resampled

# Resample the training data
